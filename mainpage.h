/** @mainpage LED Matrix Racing Game
 *
 * @author  Andrew Watson (andy@watsons.ch)
 *
 * @date    October 2011
 *
 * @section intro Description
 *
 * This project is a simple game to be played on an 8x8 bi-color LED matrix. It
 * is designed for Robopoly's LED Matrix Module, which was also designed by
 * Andrew Watson. This module features a two-colour LED matrix in the centre of
 * a shield that plugs on top of the PRisme microcontroller platform, surrounded
 * by four tactile switches.
 *
 * Using the two bottom-most switches, the user moves a "car" (symbolized by a
 * red dot on the bottom line of the matrix) through a scrolling circuit (made
 * of green dots). It is meant primarily as a demo for those who wish to get
 * started using the LED matrix library which ships with this demo.
 *
 * @section controls Controls
 *
 * Buttons 1 and 4 (bottom left and bottom right) move the car left and right.
 * The functionality of the other buttons has not been decided yet. The options
 * I am considering at the moment are:
 *  -# Pause button
 *  -# Increase/decrease game speed
 *  -# Move car up/down on the screen, thus changing the score and difficulty.
 * 
 * @section hosting Project location
 *
 * The code for this demo is currently hosted on <a
 * href="https://github.com/tunebird/matrix-race">github</a>.
 *
 * The present documentation files are hosted as <a
 * href="http://tunebird.github.com/matrix-race">github pages</a>.
 *
 * Documentation and design files for the hardware, as well as links to
 * available demos and libraries, can be found at the <a 
 * href="http://wiki.robopoly.ch/w/Matrice_Bicolore">Robopoly Wiki</a>.
 *
 */
